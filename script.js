const siteHeader = document.querySelector('.site-header');
const navToggle = document.querySelector('.nav-toggle');
const navMenu = document.querySelector('.nav-menu');
const languageToggle = document.querySelector('#language-toggle');
const countdownDays = document.querySelector('#countdown-days');
const countdownLabel = document.querySelector('#countdown-label');
const scrollTopLink = document.querySelector('[data-scroll-top]');

const targetDate = new Date('2026-10-01T17:00:00-06:00');
const millisecondsPerDay = 1000 * 60 * 60 * 24;
const mobileNavMediaQuery = window.matchMedia('(max-width: 800px)');
const scrollDeltaBeforeToggle = 8;

let currentLanguage = 'es';
let lastScrollPosition = window.scrollY;

const getTranslation = (lang, key) => {
  return key.split('.').reduce((value, part) => value?.[part], translations[lang]);
};

const applyTranslations = (lang) => {
  currentLanguage = translations[lang] ? lang : 'es';
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    const translation = getTranslation(currentLanguage, key);
    if (!translation) {
      return;
    }

    if (element.dataset.i18nHtml === 'true') {
      element.innerHTML = translation;
    } else {
      element.textContent = translation;
    }
  });

  document.querySelectorAll('[data-i18n-attr]').forEach((element) => {
    const pairs = element.dataset.i18nAttr.split('|');

    pairs.forEach((pair) => {
      const [attribute, key] = pair.split(':');
      const translation = getTranslation(currentLanguage, key);
      if (attribute && translation) {
        element.setAttribute(attribute.trim(), translation);
      }
    });
  });

  updateCountdown();
};

const updateCountdown = () => {
  if (!countdownDays || !countdownLabel) {
    return;
  }

  const now = new Date();
  const difference = targetDate.getTime() - now.getTime();

  countdownDays.textContent = difference <= 0 ? '0' : String(Math.ceil(difference / millisecondsPerDay));
  countdownLabel.textContent = getTranslation(currentLanguage, 'countdown.text');
};

if (navToggle && navMenu) {
  navToggle.addEventListener('click', () => {
    const isOpen = navMenu.classList.toggle('open');
    navToggle.setAttribute('aria-expanded', String(isOpen));
  });

  navMenu.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      navMenu.classList.remove('open');
      navToggle.setAttribute('aria-expanded', 'false');
    });
  });
}

window.addEventListener('keydown', (event) => {
  if (event.key === 'Escape' && navMenu?.classList.contains('open')) {
    navMenu.classList.remove('open');
    navToggle?.setAttribute('aria-expanded', 'false');
    navToggle?.focus();
  }
});


const closeMobileMenu = () => {
  if (!navMenu?.classList.contains('open')) {
    return;
  }

  navMenu.classList.remove('open');
  navToggle?.setAttribute('aria-expanded', 'false');
};

const updateHeaderVisibility = () => {
  if (!siteHeader) {
    return;
  }

  const currentScrollPosition = window.scrollY;
  const isMobile = mobileNavMediaQuery.matches;
  const isScrollingDown = currentScrollPosition > lastScrollPosition;
  const hasScrolledEnough = Math.abs(currentScrollPosition - lastScrollPosition) > scrollDeltaBeforeToggle;

  if (!isMobile) {
    siteHeader.classList.remove('header-hidden');
    lastScrollPosition = currentScrollPosition;
    return;
  }

  if (currentScrollPosition <= 0) {
    siteHeader.classList.remove('header-hidden');
  } else if (hasScrolledEnough && isScrollingDown) {
    siteHeader.classList.add('header-hidden');
    closeMobileMenu();
  } else if (hasScrolledEnough) {
    siteHeader.classList.remove('header-hidden');
  }

  if (hasScrolledEnough || currentScrollPosition <= 0) {
    lastScrollPosition = currentScrollPosition;
  }
};

window.addEventListener('scroll', updateHeaderVisibility, { passive: true });

if (scrollTopLink) {
  scrollTopLink.addEventListener('click', (event) => {
    event.preventDefault();
    siteHeader?.classList.remove('header-hidden');
    lastScrollPosition = window.scrollY;
    window.scrollTo({ top: 0, behavior: 'smooth' });
    history.replaceState(null, '', `${window.location.pathname}${window.location.search}`);
  });
}

if (typeof mobileNavMediaQuery.addEventListener === 'function') {
  mobileNavMediaQuery.addEventListener('change', updateHeaderVisibility);
} else {
  mobileNavMediaQuery.addListener(updateHeaderVisibility);
}

if (languageToggle) {
  languageToggle.value = 'es';
  languageToggle.addEventListener('change', (event) => {
    applyTranslations(event.target.value);
  });
}

applyTranslations('es');
setInterval(updateCountdown, 60 * 1000);

const rsvpForm = document.querySelector('#rsvp-form');
const rsvpMessage = document.querySelector('#rsvp-message');
const rsvpSubmitButton = document.querySelector('#rsvp-submit');

// Paste your Google Apps Script Web App URL here if it changes.
const scriptURL = 'https://script.google.com/macros/s/AKfycbzglbZEObJ8d4tNLIW0imaGybsgA8f8Ga7JbyVdOfE8RJQkQfZavPM7CfJL0kdUUkdH/exec';

const setRsvpMessage = (message, isError = false) => {
  if (!rsvpMessage) {
    return;
  }

  rsvpMessage.textContent = message;
  rsvpMessage.classList.toggle('error', isError);
  rsvpMessage.classList.toggle('success', !isError && Boolean(message));
};

if (rsvpForm && rsvpSubmitButton) {
  rsvpForm.addEventListener('submit', async (event) => {
    event.preventDefault();

    const nameInput = rsvpForm.querySelector('[name="name"]');
    const attendanceInput = rsvpForm.querySelector('[name="attendance"]');

    const name = String(nameInput?.value || '').trim();
    const attendance = String(attendanceInput?.value || '').trim();

    if (!name || !attendance) {
      setRsvpMessage('Please complete your full name and attendance status.', true);
      return;
    }

    const formData = new FormData(rsvpForm);

    rsvpSubmitButton.disabled = true;
    setRsvpMessage('');

    try {
      await fetch(scriptURL, {
        method: 'POST',
        mode: 'no-cors',
        body: formData,
      });

      setRsvpMessage('Thank you! Your reservation has been submitted.');
      rsvpForm.reset();
    } catch (error) {
      console.error(error);
      setRsvpMessage('Something went wrong. Please try again.', true);
    } finally {
      rsvpSubmitButton.disabled = false;
    }
  });
}
